#include <bits/stdc++.h>  // 万能头文件，包含所有C++标准库
using namespace std;

const int nn = 100005;  // 定义数组最大长度，适配1e5的数据范围
char s[nn];             // 存储输入的数字字符串

int main() {
    // 两行代码：加速cin/cout，处理1e5大数据不超时
    cin.tie(0);
    ios::sync_with_stdio(0);

    cin >> s;           // 读取输入的数字字符串

    int brk = 0, l = 0; // brk=窗口内破坏点数量；l=滑动窗口左指针
    int64_t ans = 0;    // ans=最终答案，必须用长整型（1e5的子串数会超int范围）

    // r=滑动窗口右指针，遍历字符串每一个字符（右端点）
    for (int r = 0; s[r]; r++) {
        // --------------------------
        // 1. 统计当前新增的破坏点
        // --------------------------
        // r>0时，才存在相邻字符（r=0是第一个字符，无破坏点）
        // 判断：s[r-1]和s[r] 是否破坏规则（不相等、且不是+1）
        if (r && s[r - 1] + 1 != s[r] && s[r - 1] != s[r])
            brk++;  // 破坏规则 → 破坏点+1

        // --------------------------
        // 2. 维护窗口：破坏点>1时，右移左指针l
        // --------------------------
        while (brk > 1) {  // 破坏点超过1，窗口不合法
            l++;           // 左指针右移，缩小窗口
            // 左指针右移后，移除了s[l-1]和s[l]的相邻关系
            // 如果这对字符原本是破坏点，破坏点数量-1
            if ((s[l] != s[l - 1] && s[l - 1] + 1 != s[l]))
                brk--;
        }

        // --------------------------
        // 3. 累加答案：以r结尾的好串数量 = r-l+1
        // --------------------------
        ans += r - l + 1;
    }

    cout << ans << '\n';  // 输出最终答案
    return 0;
}