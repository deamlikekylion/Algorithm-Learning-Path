#include<bits/stdc++.h>
using namespace std;

const int N=1e4+5;
int n,s[N][N],ans;  //s为二维差分数组

int main(){
    cin>>n;
    for(int i=1;i<=n;i++){
        int x1,y1,x2,y2;  cin>>x1>>y1>>x2>>y2;
        x1++;  y1++;  x2++; y2++;  //x和y的值可以为0,为了防止后面i-1,j-1=-1,所以x++从1开始
        s[x1][y1]+=1;  //二维差分核心操作:对矩形(x1,y1)~(x2,y2)区域进行标记
        s[x1][y2]-=1;  //原理:差分的"加标记"操作,最终通过前缀和还原出每个点的覆盖次数
        s[x2][y1]-=1;
        s[x2][y2]+=1;
    }
    //遍历每个面积为1的点,计算二维前缀和,还原每个点的覆盖次数,并统计被覆盖的点的数量,即要求的面积
    for(int i=1;i<N;i++){
        for(int j=1;j<N;j++){
            s[i][j]+=s[i-1][j]+s[i][j-1]-s[i-1][j-1];
            //二维前缀和公式:当前点=上方+左方-左上+自身原始值
            if(s[i][j])  ans++;
            //只要覆盖次数>0,说明该点被至少一个矩形覆盖,计入总面积(每个点代表1x1的单位面积)
        }
    }
    cout<<ans;
    return 0;
}